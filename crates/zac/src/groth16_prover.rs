/// `Σ p_odd_T[i] · bases[i]` over G1 using arkworks Pippenger MSM.
fn msm_g1(bases: &[G1Affine], scalars: &[Fr]) -> ZacResult<G1Projective> {
    if bases.len() != scalars.len() {
        return Err(ZacError::ProofRejected {
            reason: "msm_g1: bases.len() != scalars.len()",
        });
    }
    G1Projective::msm(bases, scalars).map_err(|_| ZacError::ProofRejected {
        reason: "msm_g1: arkworks MSM rejected (lengths)",
    })
}

fn msm_g2(bases: &[G2Affine], scalars: &[Fr]) -> ZacResult<G2Projective> {
    if bases.len() != scalars.len() {
        return Err(ZacError::ProofRejected {
            reason: "msm_g2: bases.len() != scalars.len()",
        });
    }
    G2Projective::msm(bases, scalars).map_err(|_| ZacError::ProofRejected {
        reason: "msm_g2: arkworks MSM rejected (lengths)",
    })
}

/// `R^(−1) mod r` where `R = 2^256` is the snarkjs/iden3 Montgomery
/// constant. Re-derives the value by doubling `1` 256 times to get `R mod r`
/// and inverting — keeps this self-contained and lets us avoid pulling
/// snarkjs's constant into the workspace as a magic number.
fn r_inv_fr() -> Fr {
    let mut r = Fr::one();
    for _ in 0..256 {
        r += r;
    }
    r.inverse().expect("2^256 mod r is nonzero")
}

/// snarkjs's "double-cover" coset evaluation:
///
///   inc = power == Fr.s ? Fr.shift : Fr.w[power+1]
///
/// We compute `inc` by walking the snarkjs `w[]` table:
///   * `w[s]   = Fr::TWO_ADIC_ROOT_OF_UNITY`        (primitive 2^s-th root)
///   * `w[i]   = w[i+1]^2`                          (descend)
///   * `w[s+1]` does NOT exist (s is the 2-adicity), so when `power == s`
///     snarkjs falls back to `Fr.shift = nqr² = 25` (since BN254 Fr `nqr = 5`).
fn compute_coset_shift(domain_size: usize) -> Fr {
    let power = (domain_size.trailing_zeros()) as usize;
    let s = Fr::TWO_ADICITY as usize;
    if power == s {
        // nqr² mod r. BN254 Fr's smallest quadratic non-residue is 5;
        // 5² = 25.
        return Fr::from(25u64);
    }
    // power < s: descend from Fr.w[s] to Fr.w[power+1] via repeated squaring.
    // w[s] = TWO_ADIC_ROOT_OF_UNITY, w[s-1] = w[s]², …, w[power+1] = w[power+2]².
    let mut w = Fr::TWO_ADIC_ROOT_OF_UNITY;
    let steps = s - (power + 1);
    for _ in 0..steps {
        w = w.square();
    }
    w
}

/// Equivalent to snarkjs's `IFFT → batchApplyKey(_, 1, inc) → FFT` pipeline
/// applied to a size-N evaluation vector `evals`.
///
/// Mathematically: let `p(X)` be the unique poly of degree ≤ N−1 with
/// `p(ω^i) = evals[i]`. Then the returned vector equals
/// `[p(inc · ω^i)]_{i=0..N}`. Useful for QAP "double-cover" evaluations.
fn coset_evaluations(domain: &Radix2EvaluationDomain<Fr>, evals: &[Fr], inc: Fr) -> Vec<Fr> {
    // 1. IFFT to recover monomial coefficients.
    let coeffs = domain.ifft(evals);
    // 2. Scale coefficients by inc^i ("batchApplyKey").
    let mut scaled = coeffs;
    let mut power = Fr::one();
    for c in scaled.iter_mut() {
        *c *= power;
        power *= inc;
    }
    // 3. Forward FFT to get evaluations on the coset {inc · ω^i}.
    domain.fft(&scaled)
}

fn fr_le_hex(x: &Fr) -> String {
    use ark_ff::BigInteger;
    let bytes = x.into_bigint().to_bytes_le();
    hex::encode(bytes)
}

#[cfg(test)]
mod tests {
    use super::*;
    use ark_ff::UniformRand;

    #[test]
    fn coset_shift_squared_equals_domain_gen_for_subdomain() {
        // Smoke: for power < Fr::TWO_ADICITY, compute_coset_shift(n)
        // should satisfy `inc² == Radix2EvaluationDomain(n).group_gen`.
        // (snarkjs invariant: Fr.w[power+1]² == Fr.w[power].)
        for &n in &[2usize, 4, 8, 16, 64, 1024] {
            let dom = Radix2EvaluationDomain::<Fr>::new(n).unwrap();
            let inc = compute_coset_shift(n);
            assert_eq!(
                inc.square(),
                dom.group_gen,
                "inc² must equal group_gen for n={n}"
            );
        }
    }

    #[test]
    fn coset_evaluations_match_direct_eval() {
        // Sanity: build a random degree-(n-1) polynomial, evaluate it at
        // ω^i and then run coset_evaluations to evaluate at α·ω^i.
        // Compare to a direct Horner evaluation.
        let mut rng = ChaCha20Rng::seed_from_u64(42);
        let n = 8usize;
        let dom = Radix2EvaluationDomain::<Fr>::new(n).unwrap();
        let coeffs: Vec<Fr> = (0..n).map(|_| Fr::rand(&mut rng)).collect();
        // Build evals = [p(ω^i)] manually.
        let omega = dom.group_gen;
        let mut evals = Vec::with_capacity(n);
        let mut x = Fr::one();
        for _ in 0..n {
            evals.push(eval_horner(&coeffs, x));
            x *= omega;
        }
        // Use coset_evaluations to get [p(inc · ω^i)].
        let inc = compute_coset_shift(n);
        let got = coset_evaluations(&dom, &evals, inc);
        // Direct check.
        let mut want = Vec::with_capacity(n);
        let mut y = inc;
        for _ in 0..n {
            want.push(eval_horner(&coeffs, y));
            y *= omega;
        }
        assert_eq!(got, want);
    }

    fn eval_horner(coeffs: &[Fr], x: Fr) -> Fr {
        let mut acc = Fr::zero();
        for c in coeffs.iter().rev() {
            acc = acc * x + *c;
        }
        acc
    }
}
